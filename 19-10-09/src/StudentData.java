
public class StudentData {

	private String name;
	private int age;
	private String ID;
	private String gender;
	
//Sets
	public void setName(String input) {
		this.name = input;
	}
	public void setAge(int input) {
		this.age = input;
	}
	public void setID(String input) {
		this.ID = input;
	}
	public void setGender(String input) {
		this.gender = input;
	}
//Gets
	public String getName() {
		return this.name;
	}
	public int getAge() {
		return this.age;
	}
	public String getID() {
		return this.ID;
	}
	public String getGender() {
		return this.gender;
	}


}


